def square(num):
    return num*num
def cube(num):
    return num*num*num
print('==========')
print('Square & Cube of numbers')
print('==========')
num= int(input('Enter a number:'))
square_of_number= square(num)
print('Square of number is:', square_of_number)
cube_of_number= cube(num)
print('Cube of number is:', cube_of_number)
